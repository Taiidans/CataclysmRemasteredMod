--function to build ship list
function ListBox_Fill(tData, tOutput, sScreen)
	for i, iCount in tData do
		tOutput[i] =
		{
			type = "ListBoxItem",
			autosize = 1,
			autoarrange = 1,
			soundOnClicked = "SFX_MissionSelectClick",
			;
			{
				type = "Button",						
				autosize = 1,
				onMouseClicked = "UI_ReloadScreen('"..sScreen.."'); UI_SetElementVisible('"..sScreen.."', '"..iCount.ThingToBuild.."', 1)", 
				;					
				{
					type = "TextLabel",				
					position = {4, 0},
					size = {190, 13},
					Text = 
					{
						text = iCount.DisplayedName,
						textStyle = "FEButtonTextStyle",
						color = { 255, 255, 255, 255},
						hAlign = "Left",
						offset = {2, 0},
					},
					;
				},
			},
		}
	end
end

